package day0526;

/**
 * 简单介绍基本数据类型
 */
public class DateType {
    public static void main(String[] args) {
        //整形
        byte a = 6;     //8bit, 1字节；
        short b = 6;    //16bit，2字节
        int c = 6;      //32bit，4字节
        long d = 6L;    //64bit， 8字节

        //浮点型
        float e = 3.5F;     //单精度 32位
        double f = 3.234;   //双精度 64位


        //字符型
        char g = 'a';
        //char h = 'jfh';
        char h = '好';
        char i = '\n';


        //布尔型
        boolean result = true;
    }
}
