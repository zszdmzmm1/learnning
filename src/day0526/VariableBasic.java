package day0526;

/**
 * 解释了声明变量的方法与基本规定
 */

public class VariableBasic {
    public static void main(String[] args) {
        // 数据类型 变量名 = 值;
        int count = 2;

        //变量的使用
        System.out.println(count);

        //变量的声明
        int size;
        //System.out.println(size);

        //赋值
        size = 1001;
        System.out.println(size);

        //int size = 6; 不可复用
    }
}
